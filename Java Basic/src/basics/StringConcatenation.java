package basics;

public class StringConcatenation {

	
	public static void main(String[] args) {
		
		
		int i=10;
		int j=20;
		
		String a="Zeeshan";
		String b="Malik";
		
		double x=10.55;
		double y=90.22;
		
		
		System.out.println(i+j);
		System.out.println(i+j+a+b);
		System.out.println(a+b+i+j);
		System.out.println(a+b+(i+j));
		
		System.out.println(x+y+i);
	}
}
